// metodos para usar string

// obtener la longitud de un string mediante atributo lenght
let str_1 = "hola soy un string ejemplo";

console.log(str_1.length);

/** obtener datos de un string
 * slice ()
 * substring()
 */

let slice_str_1 = str_1.slice(0, 26);
